public class Primitives {
    
    // This method main - it's an entry point
    // psvm
    public static void main(String[] args) {
        
        // Byte value
        byte b = 12;
        System.out.println(b);
        
        // Short value
        short s = 59;
        System.out.println(s);
        
        // Integer value
        int i = 10;
        System.out.println(i);
        
        // Long value
        long l = 4003L;
        System.out.println(l);
        
        double d = 405.84d;
        System.out.println(d);
    }
    
}
